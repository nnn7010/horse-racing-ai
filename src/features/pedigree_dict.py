_warned = set()


def classify_sire_line(sire_name: str) -> str:
    """種牡馬名から系統を判定する。未登録はその他+警告。"""
    if not sire_name:
        return "その他"
    line = SIRE_LINE.get(sire_name)
    if line:
        return line
    if sire_name not in _warned:
        logger.warning(f"Unknown sire line: {sire_name}")
        _warned.add(sire_name)
    return "その他"
